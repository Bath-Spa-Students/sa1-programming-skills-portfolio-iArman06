# Exercise 7: Some Counting

#loop that counts up from 0 to 50 in increments of 1.
print("Couting Number from 0 till 50")
for i in range(0,51,1): #the 1 inside the bracked is used for increment by 1 from 0 till 50
    print(i)
   


#loop that counts down from 50 to 0 in decrements of 1.
print("\n")
print("Counting down from 50 till 0")
for j in range(50,-1,-1): #the -1 on the right inside the bracked is used for decrement by 1 from 50 till 0
    print(j)


# loop that counts up from 30 to 50 in increments of 1.
print("\n")
print("Counting up from 30 till 50")
for i in range(30,51,1):
    print(i)


#loop that counts down from 50 to 10 in decrements of 2
